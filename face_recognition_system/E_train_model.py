# Unknown Threshold Tuning
# =============================================================================
def tune_unknown_threshold(model: keras.Model,
                           unknown_data: np.ndarray,
                           known_test_data: Tuple[np.ndarray, np.ndarray],
                           class_names: List[str],
                           output_dir: str) -> float:
    """
    Tune the confidence threshold for unknown person detection.
    
    The goal is to find a threshold that:
    - Correctly rejects unknown persons (high specificity for unknown)
    - Maintains high accuracy on known persons
    
    Args:
        model: Trained Stage B model
        unknown_data: Images of unknown persons
        known_test_data: (X, y) of known persons
        class_names: List of known class names
        output_dir: Output directory
        
    Returns:
        Optimal threshold value
    """
    print(f"\n{'='*60}")
    print("Tuning Unknown Detection Threshold")
    print(f"{'='*60}")
    
    if len(unknown_data) == 0:
        print("No unknown test data available. Using default threshold: 0.70")
        return 0.70
    
    X_known, y_known = known_test_data
    
    # Get predictions for known and unknown
    known_probs = model.predict(X_known, verbose=0)
    unknown_probs = model.predict(unknown_data, verbose=0)
    
    known_max_conf = np.max(known_probs, axis=1)
    unknown_max_conf = np.max(unknown_probs, axis=1)
    
    # Test different thresholds
    thresholds = np.arange(0.50, 0.95, 0.05)
    results = []
    
    print(f"\n{'Threshold':>10} | {'Known Acc':>10} | {'Unknown Rej':>12} | {'Combined':>10}")
    print("-" * 50)
    
    for thresh in thresholds:
        # For known: accuracy on correctly classified with confidence >= threshold
        known_preds = np.argmax(known_probs, axis=1)
        known_mask = known_max_conf >= thresh
        
        if np.sum(known_mask) > 0:
            known_correct = np.sum((known_preds == y_known) & known_mask)
            known_total = np.sum(known_mask)
            known_acc = known_correct / known_total
        else:
            known_acc = 0
        
        # For unknown: rejection rate (correctly identified as unknown)
        unknown_rejected = np.sum(unknown_max_conf < thresh)
        unknown_rej = unknown_rejected / len(unknown_max_conf)
        
        # Combined score (F1-like balance)
        combined = 2 * (known_acc * unknown_rej) / (known_acc + unknown_rej + 1e-10)
        
        results.append({
            'threshold': thresh,
            'known_accuracy': known_acc,
            'unknown_rejection': unknown_rej,
            'combined_score': combined
        })
        
        print(f"{thresh:>10.2f} | {known_acc:>10.2%} | {unknown_rej:>12.2%} | {combined:>10.2%}")
    
    # Find best threshold
    best = max(results, key=lambda x: x['combined_score'])
    
    print(f"\nOptimal threshold: {best['threshold']:.2f}")
    print(f"  Known accuracy: {best['known_accuracy']:.2%}")
    print(f"  Unknown rejection: {best['unknown_rejection']:.2%}")
    
    # Save threshold tuning results
    output_path = Path(output_dir)
    with open(output_path / 'threshold_tuning.json', 'w') as f:
        json.dump({
            'optimal_threshold': best['threshold'],
            'known_accuracy': best['known_accuracy'],
            'unknown_rejection': best['unknown_rejection'],
            'all_results': results
        }, f, indent=2)
    
    return best['threshold']


# =============================================================================
# Main Training Pipeline
# =============================================================================
def main():
    parser = argparse.ArgumentParser(description='Train face recognition models')
    parser.add_argument('--data_dir', type=str, default='processed',
                        help='Directory with preprocessed data')
    parser.add_argument('--output_dir', type=str, default='models',
                        help='Directory to save trained models')
    parser.add_argument('--epochs', type=int, default=EPOCHS,
                        help='Maximum training epochs')
    parser.add_argument('--batch_size', type=int, default=BATCH_SIZE,
                        help='Training batch size')
    parser.add_argument('--train_stage', type=str, default='both',
                        choices=['a', 'b', 'both'],
                        help='Which stage to train')
    args = parser.parse_args()
    
    # Set random seeds
    np.random.seed(42)
    tf.random.set_seed(42)
    
    # Load metadata
    data_path = Path(args.data_dir)
    with open(data_path / 'metadata.json', 'r') as f:
        metadata = json.load(f)
    
    print("=" * 60)
    print("TinyML Face Recognition Training Pipeline")
    print("=" * 60)
    print(f"\nData directory: {args.data_dir}")
    print(f"Output directory: {args.output_dir}")
    print(f"Image size: {metadata['img_size']}x{metadata['img_size']}")
    
    output_path = Path(args.output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    # =================================
    # Stage A: Person Detection
    # =================================
    if args.train_stage in ['a', 'both']:
        print("\n" + "=" * 60)
        print("STAGE A: Person Detection Training")
        print("=" * 60)
        
        # Load Stage A data
        stage_a_data = load_data(args.data_dir, 'a')
        class_names_a = metadata['class_names_stage_a']
        
        print(f"\nClasses: {class_names_a}")
        print(f"Train: {len(stage_a_data['train'][0])}")
        print(f"Val: {len(stage_a_data['val'][0])}")
        print(f"Test: {len(stage_a_data['test'][0])}")
        
        # Create and train model
        model_a = create_stage_a_model()
        print_model_analysis(model_a)
        
        history_a = train_model(
            model_a,
            stage_a_data['train'],
            stage_a_data['val'],
            'stage_a',
            args.output_dir,
            epochs=args.epochs,
            batch_size=args.batch_size
        )
        
        # Plot training history
        plot_training_history(history_a, 'Stage A',
                              str(output_path / 'stage_a_history.png'))
        
        # Evaluate
        acc_a, cm_a = evaluate_model(
            model_a,
            stage_a_data['test'],
            class_names_a,
            'stage_a',
            args.output_dir
        )
        
        # Save final model
        model_a.save(output_path / 'stage_a_final.keras')
        print(f"\nStage A model saved to: {output_path / 'stage_a_final.keras'}")
    
    # =================================
    # Stage B: Face Recognition
    # =================================
    if args.train_stage in ['b', 'both']:
        print("\n" + "=" * 60)
        print("STAGE B: Face Recognition Training")
        print("=" * 60)
        
        # Load Stage B data
        stage_b_data = load_data(args.data_dir, 'b')
        class_names_b = metadata['class_names_stage_b']
        num_classes = len(class_names_b)
        
        print(f"\nClasses: {class_names_b}")
        print(f"Number of classes: {num_classes}")
        print(f"Train: {len(stage_b_data['train'][0])}")
        print(f"Val: {len(stage_b_data['val'][0])}")
        print(f"Test: {len(stage_b_data['test'][0])}")
        
        # Oversample minority classes before training
        X_train_b, y_train_b = augment_minority_classes(
            stage_b_data['train'][0],
            stage_b_data['train'][1],
            target_ratio=0.85
        )
        stage_b_data['train'] = (X_train_b, y_train_b)
        print(f"\nBalanced training set: {len(X_train_b)} samples")

        # Create and train model
        model_b = create_stage_b_model(num_classes=num_classes)
        print_model_analysis(model_b)

        history_b = train_model(
            model_b,
            stage_b_data['train'],
            stage_b_data['val'],
            'stage_b',
            args.output_dir,
            epochs=args.epochs,
            batch_size=args.batch_size
        )
        
        # Plot training history
        plot_training_history(history_b, 'Stage B',
                              str(output_path / 'stage_b_history.png'))
        
        # Evaluate
        acc_b, cm_b = evaluate_model(
            model_b,
            stage_b_data['test'],
            class_names_b,
            'stage_b',
            args.output_dir
        )
        
        # Tune unknown threshold
        unknown_path = data_path / 'unknown_test.npz'
        if unknown_path.exists():
            unknown_data = np.load(unknown_path)['X']
            threshold = tune_unknown_threshold(
                model_b,
                unknown_data,
                stage_b_data['test'],
                class_names_b,
                args.output_dir
            )
            
            # Update metadata with threshold
            metadata['unknown_threshold'] = threshold
        else:
            print("\nNo unknown test data found. Using default threshold: 0.70")
            metadata['unknown_threshold'] = 0.70
        
        # Save final model
        model_b.save(output_path / 'stage_b_final.keras')
        print(f"\nStage B model saved to: {output_path / 'stage_b_final.keras'}")
    
    # Save updated metadata
    with open(output_path / 'training_metadata.json', 'w') as f:
        json.dump(metadata, f, indent=2)
    
    print("\n" + "=" * 60)
    print("Training Complete!")
    print("=" * 60)
    print(f"\nNext steps:")
    print(f"  1. Convert to TFLite: python F_quantize_model.py --model_dir {args.output_dir}")
    print(f"  2. Deploy to Arduino: See G_arduino_firmware.ino")


if __name__ == '__main__':
    main()
