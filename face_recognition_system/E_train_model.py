#!/usr/bin/env python3
"""
E. Training Pipeline for Two-Stage Face Recognition

This script trains both Stage A and Stage B models:
1. Stage A: Person vs No-Person (Binary)
2. Stage B: 5 Known Persons (Multi-class)

Features:
- Early stopping and learning rate reduction
- Class weighting for imbalanced data
- Confusion matrix and accuracy metrics
- Model checkpointing

Usage:
    python E_train_model.py --data_dir processed --epochs 100
"""

